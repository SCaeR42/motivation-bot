# Функция для генерации случайного мотивационного сообщения
def generate_motivation():
    # return random.choice(motivational_quotes)
    # Пример использования
    file_path = 'motivational_quotes.txt'  # Путь к файлу
    return get_random_message_from_file(file_path)


def get_random_message_from_file(file_path):
    try:
        # Открываем файл для чтения
        with open(file_path, 'r', encoding='utf-8') as file:
            # Читаем все строки в файле и удаляем лишние пробелы и символы новой строки
            quotes = [line.strip() for line in file.readlines()]
        
        # Если в файле есть фразы, выбираем одну случайную
        if quotes:
            return random.choice(quotes)
        else:
            return "Фразы не найдены в файле."
    
    except FileNotFoundError:
        return "Файл не найден."
    except Exception as e:
        return f"Произошла ошибка: {e}"



